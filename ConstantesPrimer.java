
public class ConstantesPrimer {
	public static void main(String args[]) {
		double numeroDecimal = 12.48;
        System.out.println("Valor original de double: " + numeroDecimal);

       
        int numeroEntero = (int) numeroDecimal;
        System.out.println("Valor convertido de double a int: " + numeroEntero);

       
        int otroEntero = 65;
        System.out.println("Valor original de int: " + otroEntero);

       
        double otroDecimal = (double) otroEntero;
        System.out.println("Valor convertido de int a double: " + otroDecimal);
	}
}
